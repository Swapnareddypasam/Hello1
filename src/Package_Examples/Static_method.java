package Package_Examples;

public class Static_method {
	
		static void add()
		{
		int a=10;
		int b=13;
		System.out.println(a+b);
		}
		public static void sub()
		{
		int x=50;
		int y=13;
		System.out.println(x-y);
		}
		public static void main(String args[])
		{
			Static_method.sub();
			Static_method.add();	
		}
		}


