package Package_Examples;

public class Swapping_twonumbers {
	public static void main(String args[]) {
		
	

	int a = 10;
	int b = 20;
	
	System.out.println("Before swapping  a = "+a+"  and b =  "+b);
	a = a + b; //30
	b = a - b; //10
	a = a - b; //20
	
	
	
	System.out.println("After swappin  a = " + a +" and  b = "+b);	
	
	

}
}

